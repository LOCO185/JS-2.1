function describeCountry(country, population, capitalCity){
    return `${country} has ${population} people and its capital is ${capitalCity}`;

}
console.log(describeCountry('finland','6 million', 'helsinki.'));
console.log(describeCountry('israel','7 million', 'jerusalem.'));
console.log(describeCountry('USA','140 million', 'Washington DC.'));




